    // make sure all the lecturer Id is unique
    public String createId(){
        String[] storedID = new String[999999];
        String uniqueID = "";
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\textfile\\lecturer.txt"))) {
            String line;
            int index = 0;
            while ((line = reader.readLine()) != null) {
                String[] fields = line.split(",");
                storedID[index++] = fields[0].split("C")[1];
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Opps, error happen! ");
        }
        for (int i = 1; i <= 999999; i++) {
            String numericID = String.format("%06d", i);
            boolean found = false;
            for (String id : storedID) {
                if (numericID.equals(id)) {
                    found = true;
                    break;
                }
            }
            if (!found) {
                uniqueID = numericID;
                break;
            }
        }
        return uniqueID;
    }
