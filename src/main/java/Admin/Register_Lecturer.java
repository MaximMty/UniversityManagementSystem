    private void RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegisterActionPerformed
        Admin admin = new Admin();
        boolean error = false;
        String name = "";
        String nationality="";
        String gender="";
        String department="";
        String id="";
        String password="";
        while(!error){
            if(NameTxt.getText() != null){
                name = NameTxt.getText();
            }else{
                JOptionPane.showMessageDialog(null,"User's name have to be input. ");
                error = true;
            }
            if(NationalityTxt.getText() != null){
                nationality = NationalityTxt.getText();
            }else{
                JOptionPane.showMessageDialog(null,"User's nationality have to be input. ");
                error = true;
            }
            if(MaleRadio.isSelected() || FemaleRadio.isSelected()){
                if(MaleRadio.isSelected()){
                    gender = MaleRadio.getText();
                }else{
                    gender = FemaleRadio.getText();
                }
            }else{
                JOptionPane.showMessageDialog(null,"At least select one gender.  ");
                error = true;
            }
            department = DepartmentCombo.getSelectedItem().toString();
            if(!"".equals(createId())){
                id = "LC" + createId();
            }
            else{
                JOptionPane.showMessageDialog(null,"No more unique ID for new lecturer. ");
                error = true;
            }
            
            if (error) {
                break; // Break the loop if an error occurs
            }
            password = name + id.substring(id.length() - 3);
            admin.registerLecturer(id, password, name, gender, nationality, department);
            JOptionPane.showMessageDialog(null,"You have successfully register a new lecturer. ");
            break;
        }
    }//GEN-LAST:event_RegisterActionPerformed

