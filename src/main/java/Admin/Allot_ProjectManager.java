    public void AddSelectedInfo(Lecturer lecturer){
        ShowIdLbl.setText(lecturer.getId());
        ShowNameLbl.setText(lecturer.getName());
        ShowDepartmentLbl.setText(lecturer.getDepartment());
        if(lecturer.getAssignedProjectManager().equals("true")){
            AssignCheck.setSelected(true);
        }else{
            AssignCheck.setSelected(false);
        }
    }

