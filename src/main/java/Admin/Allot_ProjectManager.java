    private void ConfirmBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmBttnActionPerformed
        String assign= "";
        Admin admin = new Admin();
        if(AssignCheck.isSelected()){
            assign = "true";
        }else{
            assign = "false";
        }
        admin.allotProjectManager(Lecturer_Info.getLecturer().getId(), Lecturer_Info.getLecturer().getPassword(), 
                Lecturer_Info.getLecturer().getName(), Lecturer_Info.getLecturer().getNationality(), 
                Lecturer_Info.getLecturer().getGender(), Lecturer_Info.getLecturer().getDepartment(), assign);
        JOptionPane.showMessageDialog(null,"You have allot/remove project manager role to/from a lecturer");
    }//GEN-LAST:event_ConfirmBttnActionPerformed

