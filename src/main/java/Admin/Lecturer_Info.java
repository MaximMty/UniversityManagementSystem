    private void EditLecturerBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditLecturerBttnActionPerformed
        // Get the index of the selected row
        int selectedRow = LecturerTable.getSelectedRow();

        if (selectedRow != -1) { // -1 indicates no row is selected
            // Get data from the selected row
            Object[] rowData = new Object[LecturerTable.getColumnCount()];
            for (int i = 0; i < LecturerTable.getColumnCount(); i++) {
                rowData[i] = LecturerTable.getValueAt(selectedRow, i);
            }
            // save the lecturer info which are selected from the table to pass it to the next form
            Lecturer_Info.editingLecturer = new Lecturer(rowData[0].toString(),rowData[1].toString(),"lecturer",rowData[2].toString(),rowData[3].toString(),rowData[4].toString(),rowData[5].toString(),rowData[6].toString());
            this.dispose();
            new Edit_Lecturer().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null,"You should select a row. ");
        }
    }//GEN-LAST:event_EditLecturerBttnActionPerformed

    private void DeleteBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteBttnActionPerformed
        DefaultTableModel model = (DefaultTableModel) LecturerTable.getModel();
        Admin admin = new Admin();
        int selectedRow = LecturerTable.getSelectedRow();
        String selectedId = "";
        if (selectedRow != -1) { // -1 indicates no row is selected
            // Get data from the selected row
            Object[] rowData = new Object[LecturerTable.getColumnCount()];
            for (int i = 0; i < LecturerTable.getColumnCount(); i++) {
                rowData[i] = LecturerTable.getValueAt(selectedRow, i);
            }
            selectedId = rowData[0].toString();
            admin.deleteLecturer(selectedId);
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(null,"You have deleted this user. ");
        } else {
            JOptionPane.showMessageDialog(null,"You should select a row. ");
        }
    }//GEN-LAST:event_DeleteBttnActionPerformed

