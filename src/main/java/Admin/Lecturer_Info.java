    public void addInfoToTable(){
        DefaultTableModel model = (DefaultTableModel) LecturerTable.getModel();
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\textfile\\lecturer.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] fields = line.split(",");
                model.addRow(fields);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
