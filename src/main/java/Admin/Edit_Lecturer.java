    private void EditBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBttnActionPerformed
        boolean error = false;
        Admin admin = new Admin();
        String password = "";
        String name = "";
        String nationality="";
        String gender="";
        String department="";
        String id="";
        while(!error){
            if(NameTxt.getText() != null){
                name = NameTxt.getText();
            }else{
                JOptionPane.showMessageDialog(null,"User's name have to be input. ");
                error = true;
            }
            if(NationalityTxt.getText() != null){
                nationality = NationalityTxt.getText();
            }else{
                JOptionPane.showMessageDialog(null,"User's nationality have to be input. ");
                error = true;
            }
            if(MaleRadio.isSelected() || FemaleRadio.isSelected()){
                if(MaleRadio.isSelected()){
                    gender = MaleRadio.getText();
                }else{
                    gender = FemaleRadio.getText();
                }
            }else{
                JOptionPane.showMessageDialog(null,"At least select one gender.  ");
                error = true;
            }
            department = DepartmentCombo.getSelectedItem().toString();
            if (error) {
                break; // Break the loop if an error occurs
            }
            id = ShowIdLbl.getText();
            password = ShowPasswordLbl.getText();
            admin.editLecturer(id, password, name, gender, nationality,department,Lecturer_Info.getLecturer().getAssignedProjectManager());
            JOptionPane.showMessageDialog(null,"You have edit the details. ");
            break;
        }
    }//GEN-LAST:event_EditBttnActionPerformed

