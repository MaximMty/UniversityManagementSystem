    private void LoginBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginBttnActionPerformed
        String username = UsernameTxt.getText();
        char[] passwordChars = PasswordTxt.getPassword();
        String password = new String(passwordChars);
        String authentication = Authentication(username,password);
        if(authentication == null){
            JOptionPane.showMessageDialog(null,"Invalid Credentials. Try again ");
        }
        else{
            String[] credentials = authentication.split(" ");
            JOptionPane.showMessageDialog(null,"Welcome to the system " + credentials[0] + ".");
            Login.saveId = credentials[0];
            Login.saveRole = credentials[1];
            roleConfirmation(credentials[1]);
        }
    }//GEN-LAST:event_LoginBttnActionPerformed

