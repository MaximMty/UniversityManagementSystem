    public String Authentication(String username, String password) {
        String row = null;
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\textfile\\users.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] fields = line.split(",");
                if (username.equals(fields[0].trim()) && password.equals(fields[1].trim())) {
                    row = fields[0] + " " + fields[2];
                    break;
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Opps, error happen! ");
        }
        // Return the list of user data
        return row;
    }
    
    public void roleConfirmation(String role){
        switch(role){
            case "admin":
                this.dispose();
                new Admin_HomePage().setVisible(true);
                break;
            case "student":
                this.dispose();
                new Student_HomePage().setVisible(true);
                break;
            case "project":
                this.dispose();
                new ProjectManager_HomePage().setVisible(true);
                break;
            case "lecturer":
                this.dispose();
                new Lecturer_HomePage().setVisible(true);
                break;
        }
    }
    
