    private void GradeBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GradeBttnActionPerformed
        int selectedRow = SubmissionTable.getSelectedRow();

        if (selectedRow != -1) { // -1 indicates no row is selected
            // Get data from the selected row
            Object[] rowData = new Object[SubmissionTable.getColumnCount()];
            for (int i = 0; i < SubmissionTable.getColumnCount(); i++) {
                rowData[i] = SubmissionTable.getValueAt(selectedRow, i);
            }
            // only allow supervisor to grade submission that havent been graded
            if(rowData[2].equals("not graded")){
                IndividualReport_Detail.submission = new Submission("NONE(INDIVIDUAL)",ShowAssessmentLbl.getText(),ShowStudentIdLbl.getText(),rowData[0].toString(),rowData[1].toString());
                this.dispose();
                new AllotGrade("individual").setVisible(true);
            }else{
                JOptionPane.showMessageDialog(null,"This submission already been graded");
            }

        } else {
            JOptionPane.showMessageDialog(null,"You should select a row. ");
        }
    }//GEN-LAST:event_GradeBttnActionPerformed

