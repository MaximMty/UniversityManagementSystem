    public void addInfoIntoTable(){
        DefaultTableModel model = (DefaultTableModel) SubmissionTable.getModel();
    
        // fetch information from reports.txt
        try (BufferedReader readers = new BufferedReader(new FileReader("src\\textfile\\report.txt"))) {
            String reportLine;
            while ((reportLine = readers.readLine()) != null) {
                String[] reportFields = reportLine.split(",");
                String status = "not graded";
                if (reportFields[1].equals(ShowStudentIdLbl.getText()) && reportFields[2].equals(ShowAssessmentLbl.getText())) {
                    
                    if(!reportFields[6].equals("none")){
                        status = "graded (" + reportFields[6] + ")";
                    }
                    model.addRow(new Object[]{reportFields[3],reportFields[4],status});
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Opps, error happen! ");
        }

    }
