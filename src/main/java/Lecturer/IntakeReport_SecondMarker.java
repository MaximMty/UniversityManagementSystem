    private void AcceptBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptBttnActionPerformed
        int selectedRow = SubmissionTable.getSelectedRow();

        if (selectedRow != -1) { // -1 indicates no row is selected
            // Get data from the selected row
            Object[] rowData = new Object[SubmissionTable.getColumnCount()];
            for (int i = 0; i < SubmissionTable.getColumnCount(); i++) {
                rowData[i] = SubmissionTable.getValueAt(selectedRow, i);
            }
            if(!rowData[3].equals("not graded")){
                // save the submission details that are chosen from the table
                IntakeReport_SecondMarker.submission = new Submission(ShowIntakeLbl.getText(),rowData[0].toString(),ShowAssessmentLbl.getText());
                Lecturer lecturer = new Lecturer();
                lecturer.acceptSecondMarker(IntakeReport_SecondMarker.getSubmission());
                JOptionPane.showMessageDialog(null,"You have done the acceptance of the marks. ");
            }else{
                JOptionPane.showMessageDialog(null,"This submission havent be graded");
            }

        } else {
            JOptionPane.showMessageDialog(null,"You should select a row. ");
        }
    }//GEN-LAST:event_AcceptBttnActionPerformed

