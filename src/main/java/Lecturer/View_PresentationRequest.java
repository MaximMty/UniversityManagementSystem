    public void addInfoToTable(){
        DefaultTableModel model = (DefaultTableModel) PresentationTable.getModel();
    
        // fetch information from presentation.txt
        try (BufferedReader readers = new BufferedReader(new FileReader("src\\textfile\\presentation.txt"))) {
            String line;
            while ((line = readers.readLine()) != null) {
                String[] fields = line.split(",");
                if (fields[3].equals(UserIDLbl.getText())) {
                    model.addRow(new Object[]{fields[0],fields[1],fields[2],fields[5]});
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Opps, error happen! ");
        }
    }
    
