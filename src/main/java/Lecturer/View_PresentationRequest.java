    private void ValidateBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ValidateBttnActionPerformed
        // Get the index of the selected row
        int selectedRow = PresentationTable.getSelectedRow();

        if (selectedRow != -1) { // -1 indicates no row is selected
            // Get data from the selected row
            Object[] rowData = new Object[PresentationTable.getColumnCount()];
            for (int i = 0; i < PresentationTable.getColumnCount(); i++) {
                rowData[i] = PresentationTable.getValueAt(selectedRow, i);
            }
            // save the presentation request details that are chosen from the table
            View_PresentationRequest.presentationReq = new Presentation_Request(rowData[0].toString(),rowData[1].toString(),rowData[2].toString());
            this.dispose();
            new ValidateRequest().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null,"You should select a row. ");
        }
    }//GEN-LAST:event_ValidateBttnActionPerformed

