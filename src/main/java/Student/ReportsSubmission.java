    public void addInfo(){
        String action = ViewAssessment.getAction();
        String assessmentType = ViewAssessment.getReport().getAssessmentType();
        String supervisor = ViewAssessment.getReport().getSupervisor();
        String filepath = ViewAssessment.getReport().getFilePath();
        if(action.equals("submit")){
            ShowAssessmentLbl.setText(assessmentType);
            ShowSupervisorLbl.setText(supervisor);
        }
        else if(action.equals("edit")){
            ShowAssessmentLbl.setText(assessmentType);
            ShowSupervisorLbl.setText(supervisor);
            FilePathTxt.setText(filepath);
        }
    }
