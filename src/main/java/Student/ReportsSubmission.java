    private void SubmitBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitBttnActionPerformed
        boolean error = false;
        while(!error){
            if(FilePathTxt.getText() == null){
                error = true;
                JOptionPane.showMessageDialog(null,"You should choose a file to be attached. ");
            }else{
                Student student = new Student();
                if(ViewAssessment.getAction().equals("submit")){
                    student.reportSubmission(ViewAssessment.getStudent().getIntakeCode(),ShowUserIDLbl.getText(),
                        ViewAssessment.getReport().getAssessmentType(), FilePathTxt.getText(), 
                        ViewAssessment.getReport().getSupervisor(), ViewAssessment.getReport().getSecondMarker());
                    JOptionPane.showMessageDialog(null,"You have submitted the report. ");
                    break;
                }else{
                    student.reportEditing(ViewAssessment.getStudent().getIntakeCode(),ShowUserIDLbl.getText(),
                        ViewAssessment.getReport().getAssessmentType(), FilePathTxt.getText());
                    JOptionPane.showMessageDialog(null,"You have editted the report. ");
                    break;
                }
                
            }
            
            if(error){
                break;
            }
        }
    }//GEN-LAST:event_SubmitBttnActionPerformed

